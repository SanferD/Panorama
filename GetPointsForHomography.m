function [pointsFrom, pointsTo] = GetPointsForHomography(i)
	persistent mapOfAllPoints
	if isempty(mapOfAllPoints)
		mapOfAllPoints = GetAllPoints();
	end

	pointsFrom = mapOfAllPoints(i*2 - 1);
	pointsTo = mapOfAllPoints(i*2);
end

